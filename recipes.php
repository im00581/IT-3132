              <li>1 teaspoon Thai chili sauce or hot sauce</li>
              <li>½ cup bread crumbs</li>
            </ul>
            <h4>Instructions</h4>
                <ol>
                  <li>If grilling, preheat an outdoor grill for high heat, and lightly oil a sheet of aluminum foil. If
                    baking,
