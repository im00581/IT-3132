              <li>While the pasta is cooking, combine the olive oil, tomatoes and salt in a large, non-reactive skillet
                or Dutch oven over medium heat. Cover the skillet (use a baking sheet if you don’t have a better lid).
                Cook, stirring occasionally, until most of the tomatoes have started to burst out of their skins and
                the olive oil has a light red hue (about 6 to 12 minutes).</li>
              <li>Remove the skillet from the heat and stir in the cooked pasta. Let the mixture cool for a few minutes
                while you chop the basil.</li>
              <li>Stir the mozzarella balls and basil into the pasta. Add the vinegar, then taste and add additional
                vinegar and/or salt, if it doesn’t quite taste spectacular yet. For best flavor, let the mixture rest
