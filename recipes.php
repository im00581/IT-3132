                  <li>In a medium bowl, mash black beans with a fork until thick and pasty.</li>
                  <li>In a food processor, finely chop bell pepper, onion, and garlic. Then stir into mashed beans.</li>
                  <li>In a small bowl, stir together egg, chili powder, cumin, and chili sauce.</li>
                  <li>Stir the egg mixture into the mashed beans. Mix in bread crumbs until the mixture is sticky and
                    holds
                    together. Divide mixture into four patties.</li>
                  <li>If grilling, place patties on foil, and grill about 8 minutes on each side. If baking, place
                    patties on
